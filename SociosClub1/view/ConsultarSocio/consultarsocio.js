function asignar(idSocio){
    $.post("../../controller/socio.php?op=mostrar", {idSocio : idSocio}, function (data) {
        data = JSON.parse(data);
        $('#idSocio').val(data.idSocio);

        $('#mdltitulo').html('Asignar Agente');
        $("#modalasignar").modal('show');
    });
 
}

function guardar(e){
    e.preventDefault();
	var formData = new FormData($("#socio_form")[0]);
    $.ajax({
        url: "../../controller/socio.php?op=asignar",
        type: "POST",
        data: formData,
        contentType: false,
        processData: false,
        success: function(datos){
            $("#modalasignar").modal('hide');
            $('#socio_data').DataTable().ajax.reload();
        }
    });
}

